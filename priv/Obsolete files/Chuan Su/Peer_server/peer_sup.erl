-module(peer_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/0,start_fsm/1]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(MAX_RESTART,5).
-define(MAX_TIME,60).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_link()->
	
	supervisor:start_link({local,peer_sup}, ?MODULE, []).

start_fsm(My_peer_id)->
	supervisor:start_child(fsm_socket_sup, [My_peer_id]).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
	
	SupFlags ={one_for_one,?MAX_RESTART,?MAX_TIME},
	
	%% peer_server_sup 
	ChildSpec_One= {peer_server_sup,
					{peer_server_sup,start_link,[]},
					permanent,
					infinity,
					supervisor,
					[peer_server_sup]},
	%% fsm_socket_sup
	
	ChildSpec_Two= {fsm_socket_sup,
					{fsm_socket_sup,start_link,[]},
					 permanent,
					 infinity,
					 supervisor,
					[fsm_socket_sup]},
	
    {ok,{SupFlags,[ChildSpec_One,ChildSpec_Two]}}.

