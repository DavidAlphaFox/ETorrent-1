-module(fsm_socket_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/0]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(MAX_RESTART,5).
-define(MAX_TIME,60).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_link()->
	
	supervisor:start_link({local,fsm_socket_sup}, ?MODULE,[]).


%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
	
	Sup_Flags= {simple_one_for_one,?MAX_RESTART,?MAX_TIME},
	
	%% simple_one_for_one,supervisor for fsm socket handler
	
	Child_Spec ={undefined,                                  %% Internerl ID
			    {fsm_socket_handler,start_link,[]},          %% Start Function {M,F,A}
				 temporary,                                  %% Restart = permanent|transient|temporary
				 2000,                                       %% Shutdown = brutal_kill | int()>0|infinity
				 worker,                                     %% Type  = worker | supervisor
				 [fsm_socket_handler]},                      %% Module = [Modules]|dynamic
	
						  
    {ok,{Sup_Flags,[Child_Spec]}}.

